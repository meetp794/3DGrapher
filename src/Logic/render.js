const Secant = (expression) => {
    var maxIter = 200
    var epsilon = 0.5
    var p1 = 0.5
    var p = 0
    var history = {0: p1}
    var idx = 0
  
    for (var i = 1; i <= maxIter; i++) {
        var q1 = expression(p1)
        if (q1 === 0) {
            break
        }
        var q2 = expression(p1 + (Math.abs(q1)/2))
        p = p1 + (Math.abs(q1)/2) - (Math.abs(q1)/2) * (q2/(q2 - q1))
        p1 = p
        history[i] = p
        idx = i
        if (Math.abs(history[i] - history[i-1]) < epsilon) {
            break
        }
    }
  
    return history[idx]
  
}

